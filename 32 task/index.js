let fun = () => {
    let select = document.getElementById('select')
    let changeColor = document.getElementById('change-color')
    let change = document.getElementById('change')
    let body = document.getElementById('body')
    body.style.backgroundColor = localStorage.getItem('color')??''

    let bull = true

    changeColor.onclick = () => {
        if(bull){
            let arr = ['red', 'blue', 'white']

            arr.forEach((v) => {
                let div = document.createElement('div')
                div.innerHTML = v
                div.className = 'select-divs'
                div.onclick = () => {
                    changeColor.innerHTML = div.textContent
                    select.innerHTML = ''
                    bull = true
                    change.onclick = () => {
                        if(changeColor.textContent === 'red'){
                            body.style.backgroundColor = 'red'
                            localStorage.setItem('color', body.style.backgroundColor)
                        } else if(changeColor.textContent === 'blue'){
                            body.style.backgroundColor = 'blue'
                            localStorage.setItem('color', body.style.backgroundColor)
                        } else if(changeColor.textContent === 'white'){
                            body.style.backgroundColor = 'white'
                            localStorage.setItem('color', body.style.backgroundColor)
                        }
                    }
                    
                }
                select.append(div)
            })
            bull = false

        } else {
            select.innerHTML = ''
            bull = true
        }
    }
}
